const getSortedFriends = users => {
  console.log([...users].flatMap(user => user.friends));
  return [...users]
    .flatMap(user => user.friends)
    .sort((firstFriend, secondFriend) => {
      if (firstFriend[0] === secondFriend[0]) {
        for (let i = 0; i < firstFriend.length; i++) {
          if (firstFriend[i] !== secondFriend[i]) {
            return firstFriend[i] > secondFriend[i] ? 1 : -1;
          }
        }
      }
      return firstFriend[0] > secondFriend[0] ? 1 : -1;
    });
};

